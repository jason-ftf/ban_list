  def check_ban_list
    if banned_emails
      redirect_to banned_path(reason: "Your email address is prevented from signup")
    elsif banned_ips
      redirect_to banned_path(reason: "Your ip address is prevented from signup")
    end
    return
  end

  def banned_emails
    BanItem.all.map(&:email).include?(params.dig(:user, :email).split("@").last)
  end

  def banned_ips
    BanItem.all.map(&:ip).include?(request.remote_ip)
  end

  def hardcode_ip
    request.remote_ip = remote_ip
  end

